    private void jLabel18MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel18MouseClicked
        // TODO add your handling code here:
        jPanelInvalidID.setVisible(false);
        jPanelWrongIDSearch.setVisible(false);
        jPanelWrongName.setVisible(false);
        jPanelWrongID.setVisible(false);
        jPanelInvalidName.setVisible(false);
        
        if(!jTextFieldIDAdminSearch.getText().equals("")&&!jTextFieldID.getText().equals("")&&!jTextFieldName.getText().equals(""))
        {
            Validation v = new Validation();
            
            if(v.checkID(jTextFieldIDAdminSearch.getText())){
                if(v.checkID(jTextFieldID.getText())){
                    if(v.checkName(jTextFieldName.getText())){
                        Course ad = new Course();
                        String Search =ad.getCourse(Integer.parseInt(jTextFieldIDAdminSearch.getText())).getCourseName();
                        if(!jTextFieldID.getText().equals(jTextFieldIDAdminSearch.getText())||!Search.equals(jTextFieldName.getText())){
                            if(!ad.dublicateCourse(jTextFieldName.getText(), Integer.parseInt(jTextFieldID.getText()))){
                                ad.setCourseID(Integer.parseInt(jTextFieldID.getText()));
                                ad.setCourseName(jTextFieldName.getText());
                                ad.setCreditHour(jComboBoxOfficeH.getSelectedIndex()+1);
                                ad.setDept(jComboBoxDepart.getSelectedItem().toString());
                                if(ad.editCourse(Integer.parseInt(jTextFieldIDAdminSearch.getText()))){
                                    jLabelSuccessOrFail.setText("Updated Successfully ...! :)");
                                    resetPanel();
                                }
                                else{
                                    jLabelSuccessOrFail.setText("Failed Updated ...! :(");
                                    JOptionPane.showMessageDialog(null, "Not Found ID Course ,Failed Update...!\n Try Again :(");
                                    resetPanel();
                                }
                             }
                        else{
                                    jLabelSuccessOrFail.setText("Not Allowed New Course ID or Course Name :(");
                                    jPanelInvalidID.setVisible(true);
                                    JOptionPane.showMessageDialog(null, "Not Allowed Course ID or Course Name  , Course ID or Course Name is Reserved...!\n Try Again :(");
                            }
                    }
                        else{
                                ad.setCourseID(Integer.parseInt(jTextFieldID.getText()));
                                ad.setCourseName(jTextFieldName.getText());
                                ad.setCreditHour(jComboBoxOfficeH.getSelectedIndex()+1);
                                ad.setDept(jComboBoxDepart.getSelectedItem().toString());
                                if(ad.editCourse(Integer.parseInt(jTextFieldIDAdminSearch.getText()))){
                                    jLabelSuccessOrFail.setText("Updated Successfully ...! :)");
                                    resetPanel();
                                }
                                else{
                                    jLabelSuccessOrFail.setText("Failed Updated ...! :(");
                                    JOptionPane.showMessageDialog(null, "Not Found ID Course ,Failed Update...!\n Try Again :(");
                                    resetPanel();
                                }
                        }
                    }
                    else{
                        JOptionPane.showMessageDialog(null, "Not valid Name Try Again :(");
                        jPanelWrongName.setVisible(true);
                    }
                }
                else{
                    JOptionPane.showMessageDialog(null, "Not valid ID Course Try Again :(\nHint: ID Course Length should more than 4 .. ;)");
                    jPanelWrongID.setVisible(true);
                }
            
                }
            else{
                JOptionPane.showMessageDialog(null, "Not valid ID Course Try Again :(\nHint: ID Course Length should more than 4 .. ;)");
                jTextFieldIDAdminSearch.setVisible(true);
            }
        }
        else
        jLabelSuccessOrFail.setText("Missing Required Fields ... !  Please, complete them before submit ...! :(");
    }//GEN-LAST:event_jLabel18MouseClicked

