    private void jLabelSearchMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabelSearchMouseClicked
        // TODO add your handling code here:
        jTextFieldCreditHour.setEnabled(false);
        jTextFieldName.setEnabled(false);
        jPanelInvalidID.setVisible(false);
        jPanelWrongID.setVisible(false);
        if(!jTextFieldSearhKey.getText().equals("")){
            if(new Validation().checkID(jTextFieldSearhKey.getText())){
                Course c = new Course();
                if(c.searchCourse(Integer.parseInt(jTextFieldSearhKey.getText()))){
                    c=c.getCourse(Integer.parseInt(jTextFieldSearhKey.getText()));
                    setPanel(c);
                }
                else{
                    jLabelSucessOrFail.setText("Not Found :(");
                    jPanelWrongID.setVisible(true);
                    resetPanel();
                }
         }
                else{
                    JOptionPane.showMessageDialog(null, "Not valid ID Try Again :(\nHint: ID Length should more than 4 .. ;)");
                    jPanelWrongID.setVisible(true);
                }
        }
        else
            jLabelSucessOrFail.setText("Missing Required ID Course ... !  Please, complete it before Search ...! :(");
    }//GEN-LAST:event_jLabelSearchMouseClicked

