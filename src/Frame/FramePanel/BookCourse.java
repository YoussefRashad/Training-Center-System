    private void setPanel(Course c)
    {
        jTextFieldCreditHour.setText(""+c.getCreditHour());
        jTextFieldName.setText(""+c.getCourseName());
        jComboBox1.setModel(new javax.swing.DefaultComboBoxModel<>(new String[] { c.getDept() }));

    }

