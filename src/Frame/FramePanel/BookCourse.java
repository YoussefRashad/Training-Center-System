    private void jLabel22MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel22MouseClicked
        // TODO add your handling code here:
        jTextFieldCreditHour.setEnabled(false);
        jTextFieldName.setEnabled(false);
        jPanelInvalidID.setVisible(false);
        jPanelWrongID.setVisible(false);
        
        if(!jTextFieldIDStudent.getText().equals("")&&!jTextFieldCreditHour.getText().equals("")&&!jTextFieldSearhKey.getText().equals("")&&!jTextFieldName.getText().equals(""))
        {
            Student s = new Student();
            s = s.getStudent(Integer.parseInt(jTextFieldIDStudent.getText()));
            if(s!=null){
                if(!s.dublicateCourse(s, jTextFieldName.getText())){
                    if(s.SubjectCount<7 ){
                        if(s.bookCourse(Integer.parseInt(jTextFieldIDStudent.getText()), jTextFieldName.getText())){
                            jLabelSucessOrFail.setText("Added Course :) ");
                            resetPanel();
                        }
                        else
                            jLabelSucessOrFail.setText("Found Problem in adding");
                        }
                    else
                        jLabelSucessOrFail.setText("Your Subject equal 7 , You Can not adding more :( ");
                    }   
                else{
                            jLabelSucessOrFail.setText("Not Allowed This Course :(");
                            jPanelInvalidID.setVisible(true);
                            JOptionPane.showMessageDialog(null, "Not Allowed This Course , This Course is already Reserved ...!\n Try Again :(");
                    }
            }
            else{
                jLabelSucessOrFail.setText("Not Found Student , Please Enter Correct ID :)");
            }
        }
        else
            jLabelSucessOrFail.setText("Missing Required Fields ... !  Please, complete them before submit ...! :(");
        
    }//GEN-LAST:event_jLabel22MouseClicked

