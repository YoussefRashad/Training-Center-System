    private void jLabelSearchMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabelSearchMouseClicked
        // TODO add your handling code here:
        jPanelWrongIDSearch.setVisible(false);
        resetTable();
        if(!jTextFieldIDStudentSearch.getText().equals("")){
            if(new Validation().checkID(jTextFieldIDStudentSearch.getText())){
                Student x = new Student();
                x=x.getStudent(Integer.parseInt(jTextFieldIDStudentSearch.getText()));
                if(x!=null){
                    setPanel(x);
                    jLabelSuccessOrFail.setText("Founded :)");
                }
                else{
                        jLabelSuccessOrFail.setText("Not Found :(");
                        JOptionPane.showMessageDialog(null, "Not Found Try Again :(");
                    }
            }
            else{
                JOptionPane.showMessageDialog(null, "Not valid ID Student Try Again :(\nHint: ID Student Length should more than 4 .. ;)");
                jTextFieldIDStudentSearch.setVisible(true);
            }
        }
        else
        jLabelSuccessOrFail.setText("Missing Required ID Student ... !  Please, complete it before submit ...! :(");
    }//GEN-LAST:event_jLabelSearchMouseClicked

