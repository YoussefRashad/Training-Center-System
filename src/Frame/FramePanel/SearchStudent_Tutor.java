    private void setPanel(Student x)
     {
        jTextFieldID.setText(""+x.getID());
        jTextFieldName.setText(""+x.getName());
        jComboBoxDepart.setModel(new javax.swing.DefaultComboBoxModel<>(new String[] { x.getDepart() }));
        if(x.getLevel()==1)
            jComboBoxLevel.setModel(new javax.swing.DefaultComboBoxModel<>(new String[] { "Level 1" }));
        else if(x.getLevel()==2)
            jComboBoxLevel.setModel(new javax.swing.DefaultComboBoxModel<>(new String[] { "Level 2" }));
        else if(x.getLevel()==3)
            jComboBoxLevel.setModel(new javax.swing.DefaultComboBoxModel<>(new String[] { "Level 3" }));
        else
            jComboBoxLevel.setModel(new javax.swing.DefaultComboBoxModel<>(new String[] { "Level 4" }));
        jTextFieldGPA.setText(""+x.getGPA());
        setTable(x);
        
     }
     private void setTable(Student x)
     {
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        Object rowData[]= new Object[4];

            String subject[]=x.getCourses();
            double grades []=x.getGrades();
            int j=0;
            for(int i=0;i<subject.length;i++){
                rowData[0]=subject[i];
                rowData[1]=grades[j++];
                rowData[2]=grades[j++];
                rowData[3]=grades[j++];
                model.addRow(rowData);
            }
     }
     
     private void resetPanel()
     {
        jTextFieldIDStudentSearch.setText("");
        jTextFieldID.setText("");
        jTextFieldName.setText("");
        jTextFieldGPA.setText("");
        jComboBoxDepart.setModel(new javax.swing.DefaultComboBoxModel<>(new String[] {  }));
        jComboBoxLevel.setModel(new javax.swing.DefaultComboBoxModel<>(new String[] {  }));
        
        jPanelWrongIDSearch.setVisible(false);
        resetTable();
        
     }
     private void resetTable()
     {
         try{
            DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
            for(int i=0;i<4;i++)
                model.removeRow(i);
            for(int i=0;i<2;i++)
                model.removeRow(i);
            model.removeRow(0);
        }
        catch(Exception e)
        {
            ;
        }
     }
