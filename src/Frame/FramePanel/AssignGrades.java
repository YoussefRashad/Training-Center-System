    private void jLabel14MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel14MouseClicked
        // TODO add your handling code here:
        jPanelWrongCourseWork.setVisible(false);
        jPanelWrongFinalExam.setVisible(false);
        jPanelWrongSearchKey.setVisible(false);
        jPanelwrongMidTerm.setVisible(false);
        
        
        if(!jTextFieldIDAdminSearch.getText().equals("")&&!jTextFieldCourseWork.getText().equals("")&&!jTextFieldFinal.getText().equals("")
           &&!jTextFieldMidTerm.getText().equals(""))
        {
            Validation v = new Validation();
            if(v.checkID(jTextFieldIDAdminSearch.getText())){
                if(v.checkGrades(jTextFieldCourseWork.getText())){
                    if(v.checkGrades(jTextFieldMidTerm.getText())){
                        if(v.checkGrades(jTextFieldFinal.getText())){
                                        Student ad = new Student();
                                        ad=ad.getStudent(Integer.parseInt(jTextFieldIDAdminSearch.getText()));
                                        if(ad!=null){
                                            double grades[]=ad.getGrades();
                                            if(jComboBoxResgisteredCourse.getSelectedIndex()==0){
                                                grades[0]=Double.parseDouble(jTextFieldCourseWork.getText());
                                                grades[1]=Double.parseDouble(jTextFieldMidTerm.getText());
                                                grades[2]=Double.parseDouble(jTextFieldFinal.getText());
                                            }
                                            else if(jComboBoxResgisteredCourse.getSelectedIndex()==1){
                                                grades[3]=Double.parseDouble(jTextFieldCourseWork.getText());
                                                grades[4]=Double.parseDouble(jTextFieldMidTerm.getText());
                                                grades[5]=Double.parseDouble(jTextFieldFinal.getText());
                                            }
                                            else if(jComboBoxResgisteredCourse.getSelectedIndex()==2){
                                                grades[6]=Double.parseDouble(jTextFieldCourseWork.getText());
                                                grades[7]=Double.parseDouble(jTextFieldMidTerm.getText());
                                                grades[8]=Double.parseDouble(jTextFieldFinal.getText());
                                            }
                                            else if(jComboBoxResgisteredCourse.getSelectedIndex()==3){
                                                grades[9]=Double.parseDouble(jTextFieldCourseWork.getText());
                                                grades[10]=Double.parseDouble(jTextFieldMidTerm.getText());
                                                grades[11]=Double.parseDouble(jTextFieldFinal.getText());
                                            }
                                            else if(jComboBoxResgisteredCourse.getSelectedIndex()==4){
                                                grades[12]=Double.parseDouble(jTextFieldCourseWork.getText());
                                                grades[13]=Double.parseDouble(jTextFieldMidTerm.getText());
                                                grades[14]=Double.parseDouble(jTextFieldFinal.getText());
                                            }
                                            else if(jComboBoxResgisteredCourse.getSelectedIndex()==5){
                                                grades[15]=Double.parseDouble(jTextFieldCourseWork.getText());
                                                grades[16]=Double.parseDouble(jTextFieldMidTerm.getText());
                                                grades[17]=Double.parseDouble(jTextFieldFinal.getText());
                                            }
                                            else if(jComboBoxResgisteredCourse.getSelectedIndex()==6){
                                                grades[18]=Double.parseDouble(jTextFieldCourseWork.getText());
                                                grades[19]=Double.parseDouble(jTextFieldMidTerm.getText());
                                                grades[20]=Double.parseDouble(jTextFieldFinal.getText());
                                            }


                                            if(ad.editStudent(Integer.parseInt(jTextFieldIDAdminSearch.getText()))){
                                                jLabelSuccessOrFail.setText("Updated Successfully ...! :)");
                                                resetPanel();
                                            }
                                            else
                                            jLabelSuccessOrFail.setText("Failed Updated ...! :(");
                                        }
                                        else{
                                                    jLabelSuccessOrFail.setText("Not Found Student ...! :(");
                                                    JOptionPane.showMessageDialog(null, "Not Found ID Admin ...!\n Try Again :(");
                                                    resetPanel();
                                                } // End Assign & Start Elses Checks
                    }
                    else{
                        JOptionPane.showMessageDialog(null, "Not valid Final Exam Try Again :(\nHint: Final Exam  should  between Range [0,100]  .. ;)");
                        jPanelWrongFinalExam.setVisible(true);
                    }
                        }
                       else{
                           JOptionPane.showMessageDialog(null, "Not valid Mid Term Try Again :(\nHint: Mid Term should  between Range [0,100]  .. ;)");
                           jPanelwrongMidTerm.setVisible(true);
                        }
                }
                else{
                    JOptionPane.showMessageDialog(null, "Not valid Course Work Try Again :(\nHint: Course Work should  between Range [0,100]  .. ;)");
                    jPanelWrongCourseWork.setVisible(true);
                }
            }
            else{
                JOptionPane.showMessageDialog(null, "Not valid ID Admin Try Again :(\nHint: ID Admin Length should more than 4 .. ;)");
                jPanelWrongSearchKey.setVisible(true);
            }
        }
        else
        jLabelSuccessOrFail.setText("Missing Required Fields ... !  Please, complete them before submit ...! :(");
    }//GEN-LAST:event_jLabel14MouseClicked

    private void jLabelSearchMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabelSearchMouseClicked
        // TODO add your handling code here:
        jPanelWrongCourseWork.setVisible(false);
        jPanelWrongFinalExam.setVisible(false);
        jPanelWrongSearchKey.setVisible(false);
        jPanelwrongMidTerm.setVisible(false);
        jComboBoxResgisteredCourse.setEnabled(false);
        
        jTextFieldCourseWork.setEnabled(false);
        jTextFieldFinal.setEnabled(false);
        jTextFieldMidTerm.setEnabled(false);
        
        if(!jTextFieldIDAdminSearch.getText().equals("")){
            if(new Validation().checkID(jTextFieldIDAdminSearch.getText())){
                Student x = new Student();
                x=x.getStudent(Integer.parseInt(jTextFieldIDAdminSearch.getText()));
                if(x!=null){
                    setPanel(x);
                    jTextFieldCourseWork.setEnabled(true);
                    jTextFieldFinal.setEnabled(true);
                    jTextFieldMidTerm.setEnabled(true);
                    jComboBoxResgisteredCourse.setEnabled(true);
                    jLabelSuccessOrFail.setText("Founded :)");
                }
                else{
                        jLabelSuccessOrFail.setText("Not Found ...! :(");
                        JOptionPane.showMessageDialog(null, "Not Found ID Admin ...!\n Try Again :(");
                        resetPanel();
                    }
                }
            else{
                JOptionPane.showMessageDialog(null, "Not valid ID Admin Try Again :(\nHint: ID Admin Length should more than 4 .. ;)");
                jPanelWrongSearchKey.setVisible(true);
            }
        }
        else
        jLabelSuccessOrFail.setText("Missing Required ID Student ... !  Please, complete it before submit ...! :(");
    }//GEN-LAST:event_jLabelSearchMouseClicked

