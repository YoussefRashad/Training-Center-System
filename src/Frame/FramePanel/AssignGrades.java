    private void setPanel(Student x)
    {
        jTextFieldgpa.setText(""+x.getGPA());
        jTextFieldID.setText(""+x.getID());
        jTextFieldName.setText(""+x.getName());
        String subject[]=x.getCourses();
        jComboBoxResgisteredCourse.setModel(new javax.swing.DefaultComboBoxModel<>(new String[] { subject[0],subject[1],subject[2],subject[3],subject[4],subject[5],subject[6]  }));
        jComboBoxDepart1.setModel(new javax.swing.DefaultComboBoxModel<>(new String[]{x.getDepart()}));
        int level = x.getLevel();
        if(level==1)
            jComboBoxLevel1.setModel(new javax.swing.DefaultComboBoxModel<>(new String[]{"Level 1"}));
        else if(level==2)
            jComboBoxLevel1.setModel(new javax.swing.DefaultComboBoxModel<>(new String[]{"Level 2"}));
        else if (level==3)
            jComboBoxLevel1.setModel(new javax.swing.DefaultComboBoxModel<>(new String[]{"Level 3"}));
        else if(level==4)
            jComboBoxLevel1.setModel(new javax.swing.DefaultComboBoxModel<>(new String[]{"Level 4"}));

    }
