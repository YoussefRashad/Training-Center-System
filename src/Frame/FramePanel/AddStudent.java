    private void jLabel18MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel18MouseClicked
        // TODO add your handling code here:
        jPanelWrongImage.setVisible(false); 
        String filename = "student.png";

        JFileChooser chooser = new JFileChooser();
        chooser.showOpenDialog(null);

        File f = chooser.getSelectedFile();
        try{
            if (f.isFile()) {
                filename = f.getAbsolutePath();
            }
        }catch(Exception e){
            jTextFieldPath.setText("");
            JOptionPane.showMessageDialog(null, "Not valid Image Try Again :(");
            jPanelWrongImage.setVisible(true);  
        }

        jTextFieldPath.setText(filename);
        try {
            ImageIcon ii = new ImageIcon(ImageIO.read(new File(filename)));//get the image from file chooser
            jLabelImage.setIcon(ii);
        } catch (Exception ex) {
            //ex.printStackTrace();
            jTextFieldPath.setText("");
            JOptionPane.showMessageDialog(null, "Not valid Image Try Again :(");
            jPanelWrongImage.setVisible(true); 
        }
    }//GEN-LAST:event_jLabel18MouseClicked

