    private void setPanel(Student x)
    {
        jTextFieldID.setText(""+x.getID());
        jTextFieldName.setText(""+x.getName());
        jTextFieldgpa.setText(""+x.getGPA()); 
        if(x.getLevel()==1)
            jComboBoxLevel1.setModel(new javax.swing.DefaultComboBoxModel<>(new String[] { "Level 1" }));
        else if(x.getLevel()==2)
            jComboBoxLevel1.setModel(new javax.swing.DefaultComboBoxModel<>(new String[] { "Level 2" }));
        else if (x.getLevel()==3)
            jComboBoxLevel1.setModel(new javax.swing.DefaultComboBoxModel<>(new String[] { "Level 3" }));
        else
            jComboBoxLevel1.setModel(new javax.swing.DefaultComboBoxModel<>(new String[] { "Level 4" }));
        jComboBoxDepart1.setModel(new javax.swing.DefaultComboBoxModel<>(new String[] {x.getDepart() }));
      setTable(x);
        
     }
    
     private void setTable(Student x)
     {
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        Object rowData[]= new Object[4];

            String subject[]=x.getCourses();
            double grades []=x.getGrades();
            int j=0;
            for(int i=0;i<subject.length;i++){
                rowData[0]=subject[i];
                rowData[1]=grades[j++];
                rowData[2]=grades[j++];
                rowData[3]=grades[j++];
                model.addRow(rowData);
            }
     }
     private void resetTable()
     {
         try{
            DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
            for(int i=0;i<4;i++)
                model.removeRow(i);
            for(int i=0;i<2;i++)
                model.removeRow(i);
            model.removeRow(0);
        }
        catch(Exception e)
        {
            ;
        }
     }
     private void resetPanel()
     {
         jTextFieldID.setText("");
        jTextFieldName.setText("");
        jTextFieldgpa.setText(""); 
        jComboBoxLevel1.setModel(new javax.swing.DefaultComboBoxModel<>(new String[] { }));
        jComboBoxDepart1.setModel(new javax.swing.DefaultComboBoxModel<>(new String[] { }));
        resetTable();
        jPanelWrongSearchKey.setVisible(false);
        jTextFieldCourseName.setText("");
        jTextFieldIDStudentSearch.setText("");
     }
