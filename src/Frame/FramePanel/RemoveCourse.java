    private void jLabel4MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel4MouseClicked
        // TODO add your handling code here:
        jPanelWrongCourseName.setVisible(false);
        jPanelWrongSearchKey.setVisible(false);
        
        if(!jTextFieldIDStudentSearch.getText().equals("")&&!jTextFieldID.getText().equals("")&&!jTextFieldCourseName.getText().equals("")){
            if(new Validation().checkID(jTextFieldIDStudentSearch.getText())){
                if(new Validation().checkName(jTextFieldCourseName.getText())){
                    if(!jTextFieldCourseName.getText().equals("null")){
                    Student s = new Student();
                    s=s.getStudent(Integer.parseInt(jTextFieldIDStudentSearch.getText()));
                    if(s!=null){
                        int index = s.getIndexCourse(s, jTextFieldCourseName.getText());
                        if(s.SearchCourse(s, jTextFieldCourseName.getText())&& index!=-1){
                            String editCourse [] = s.getCourses();
                            double editGrades [] = s.getGrades();
                            if(index == 0){
                                editCourse[0]="null";
                                editGrades[0]=editGrades[1]=editGrades[2]=0;
                            }
                            else if(index == 1){
                                editCourse[1]="null";
                                editGrades[3]=editGrades[4]=editGrades[5]=0;
                            }
                            else if(index == 2){
                                editCourse[2]="null";
                                editGrades[6]=editGrades[7]=editGrades[8]=0;
                            }
                            else if(index == 3){
                                editCourse[3]="null";
                                editGrades[9]=editGrades[10]=editGrades[11]=0;
                            }
                            else if(index == 4){
                                editCourse[4]="null";
                                editGrades[12]=editGrades[13]=editGrades[14]=0;
                            }
                            else if(index == 5){
                                editCourse[5]="null";
                                editGrades[15]=editGrades[16]=editGrades[17]=0;
                            }
                            else if(index == 6){
                                editCourse[6]="null";
                                editGrades[18]=editGrades[19]=editGrades[20]=0;
                            }
                            s.setCourses(editCourse);
                            s.setGrades(editGrades);
                            if(s.editStudent(Integer.parseInt(jTextFieldIDStudentSearch.getText()))){
                                jLabelSuccessOrFail1.setText("Removed Course Successfully "+jTextFieldCourseName.getText()+"  done ...! :)");
                                jLabelSuccessOrFail.setText("Removed Course Successfully "+jTextFieldCourseName.getText()+"  done ...! :)");
                                resetPanel();
                            }
                            else{
                            jLabelSuccessOrFail.setText("Failed Removed ...! :(");
                            JOptionPane.showMessageDialog(null, "Not Found ID Student ,Failed Removed...!\n Try Again :(");
                            resetPanel();
                            }                            
                        }
                        else{
                            jLabelSuccessOrFail1.setText("Not Found Course Name ...! :(");
                            JOptionPane.showMessageDialog(null, "Not Found Course Name ...!\n Try Again :(");
                        }
                
                        }
                        else{
                            jLabelSuccessOrFail.setText("Not Found ...! :(");
                            JOptionPane.showMessageDialog(null, "Not Found ID Student ...!\n Try Again :(");
                            resetPanel();
                        }
                }
                else{
                    JOptionPane.showMessageDialog(null, "The Course is null Try Again :(");
                    jPanelWrongCourseName.setVisible(true);
                    }
                }
                    else{
                    JOptionPane.showMessageDialog(null, "Not valid Course Name Try Again :(");
                    jPanelWrongCourseName.setVisible(true);
                }
        }
            else{
                JOptionPane.showMessageDialog(null, "Not valid ID Student Try Again :(\nHint: ID Student Length should more than 4 .. ;)");
                jPanelWrongSearchKey.setVisible(true);
            }
            
        }
        else{
            jLabelSuccessOrFail.setText("Missing Required ID Student ... !  Please, complete it before submit ...! :(");
        }
        
    }//GEN-LAST:event_jLabel4MouseClicked


